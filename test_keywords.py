def find_topic_by_keywords(message: str) -> str:
    keyword_mapping = {
        # 기본 회사 정보
        "회사이름": "회사이름",
        "회사명": "회사이름",
        "회사 이름": "회사이름",
        "회사": "회사이름",
        "주소": "주소",
        "회사주소": "주소",
        "회사 주소": "주소",
        "대표": "대표",
        "회장": "대표",
        "대표이사": "대표",
        "회사대표": "대표",
        "설립일": "설립일",
        "창립일": "설립일",
        "회사설립일": "설립일",
        "비전": "비전",
        "회사비전": "비전",
        
        # 부서 정보
        "it지원팀": "IT지원",
        "it지원": "IT지원",
        "it부": "IT부",
        "it팀": "IT부",
        "부서": "연구개발부",
        "연구": "연구개발부",
        "개발": "연구개발부",
        "영업": "영업부",
        "판매": "영업부",
        "인사": "인사부",
        "채용": "인사부",
        "재무": "재무부",
        "회계": "재무부",
        "시스템": "IT부",
        
        # 업무 프로세스
        "휴가": "휴가신청",
        "연차": "휴가신청",
        "출장": "출장신청",
        "구매": "구매신청",
        "회의실": "회의실예약",
        
        # 복리후생
        "점심": "점심시간",
        "퇴근": "퇴근시간",
        "주차": "주차",
        "보험": "의료보험",
        
        # 연락처
        "연락처": "총무팀",
        "전화": "총무팀",
        
        # 회사 규정
        "복장": "복장규정",
        "출근": "출근시간",
        "보안": "보안규정"
    }
    sorted_keywords = sorted(keyword_mapping.items(), key=lambda x: len(x[0]), reverse=True)
    for keyword, topic in sorted_keywords:
        if keyword in message:
            return topic
    return ""

